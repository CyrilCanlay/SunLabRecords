            <h2 class="blue_text">Aude Lazzarelli</h2>
            <div class="gros_pave">
                <p>Aude est une jeune designer spécialisée en image de marque et évènementiel.</p>
                <p>Au sein du Sun Lab elle est en charge de la cohésion graphique et du design global.</p>
                <p>Elle intervient ponctuellement dans l’accompagnement de certains artiste(s) en élaborant spécialement pour eux une identité visuelle (logo, charte graphique, web communication, affiches, artwork, retouche photo) et scénique (scénographie, costumes et création d’atmosphères).</p>
                <p>Parallèlement à cela, elle est très engagée dans le milieu de la création en espace public proche des arts du visuels et de la scène d’arts vivants.</p>
