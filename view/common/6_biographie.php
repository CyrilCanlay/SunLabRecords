            <h2 class="blue_text">Sébastien Castan</h2>
            <div class="gros_pave">
                <p>Seb est la figure de proue du Sun Lab Records, passionné depuis toujours par la musique et plus  généralement par le son, il est l’initiateur du studio de production musical du boulevard Chave.</p>

                <p>Engagé depuis son plus jeune âge dans des actions socio-culturelle, son parcours est avant tout humain.</p>

                <p>Son expérience auprès des jeunes en difficulté et ses qualités d’animateur complète son talent d’ingénieur du son.</p>

                <p>Formé dans un premier temps en autodidacte, il a en parallèle, fait ses armes au sein d’un studio d’enregistrement Marseillais.</p>

                <p>Son ambition personnelle et son âme d’artiste l’ont poussé à monter son propre Home Studio en 2017 où il s’engage pour accompagner les musiciens amateurs, semi-professionnel et des artistes aguerris.</p>

                <p>Son poste au sein de la mairie de Marseille lui a permis d’étendre son activité et de réaménager le centre de loisir de Mont Redon (8e arrondissement) dont il a la charge, en le transformant en lieu de pratique musicale pour tous.</p>
            </div>

