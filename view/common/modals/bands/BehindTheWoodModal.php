                <p>Behind The Woods est un groupe de metalcore mélodique originaire des Bouches du Rhône.</p>
                <p>First Breath, leur premier album, a été très largement inspiré de ce qui se fait dans le milieu.</p>
                <p>On peut citer à titre d’exemple des groupes comme As I Lay Dying et Bullet For My Valentine.</p>
                <p>Malgré tout ce ne sont pas les seules influences où le groupe a été pioché pendant cette période.</p>
                <p>Le power d’Orden Ogan ainsi et que le death mélodique d’In Flames ont été ajoutée à cette mixture qui a donné naissance à leur premier album.</p>
                <p>Dans cette galette on peut y trouver un mélange de puissance et d’émotions, entre des riffs de guitare endiablées, des screams bien énervés, des lignes de chant qui restent en tête et enfin une batterie millimétrée qui parfait le tout.</p>
                <p>Behind The Woods se dirige aujourd’hui vers de nouveaux horizons et ont ainsi abandonné le power au profit du djent et du progressif tout en gardant une bonne base metalcore.</p>
                <p>De leurs nouvelles influences on peut ainsi citer des groupes tels que Monuments, Kadinja, August Burns Red, Bleed From Within ou encore Wage War.</p>
